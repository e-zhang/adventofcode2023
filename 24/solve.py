from sympy import Symbol, solve
x = Symbol('x')
y = Symbol('y')
z = Symbol('z')
vx = Symbol('vx')
vy = Symbol('vy')
vz = Symbol('vz')
t1 = Symbol('t1')
t2 = Symbol('t2')
t3 = Symbol('t3')

eqs = [
    x + (vx + 11) * t1 - 315268300752660,
    y + (vy - 23) * t1 - 284016300325583,
    z + (vz + 52) * t1 - 407533418983227,
    x + (vx + 239) * t2 - 393927681060873,
    y + (vy + 271) * t2 - 429508206398995,
    z + (vz + 115) * t2 - 348027409734393,
    x + (vx + 37) * t3 - 279975598233486,
    y + (vy + 9) * t3 - 285305766984543,
    z + (vz + 83) * t3 - 322446398749056,
]

symbols = [
    x, y, z, vx, vy, vz, t1, t2, t3
]

s = solve(eqs, symbols, dict=True)
print(s)
